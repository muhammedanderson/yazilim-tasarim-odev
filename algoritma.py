def hesapla(sayi1, sayi2):
    # Bu fonksiyon iki sayi ile islem yapar
    sonuc = sayi1 + sayi2
    print("Sonuc:", sonuc)

# --- SENİN EKLEYECEĞİN KISIM ---
def cikarma(sayi1, sayi2):
    sonuc = sayi1 - sayi2
    print("Çıkarma Sonucu:", sonuc) # print'i içeri aldık, böylece hata vermez.

# -------------------------------

hesapla(10, 5)
cikarma(10, 5)